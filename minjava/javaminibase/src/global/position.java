package global;

public class position {
	private int rank;
	private int slotNo;
	
	public int getRank()
	{
		return this.rank;
	}
	public void setRank(int rank)
	{
		this.rank = rank;
	}
	public int getSlot()
	{
		return this.slotNo;
	}
	public void setSlot(int slotNo)
	{
		this.slotNo = slotNo;
	}
}